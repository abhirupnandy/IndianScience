    <h2 class="mb-3 mt-3 pt-5" id="hcp">India's contribution to highly cited papers</h2>
    <p class="text-justify">
        Highly cited papers of a country are often measured by the number of publications that it contributes in  the <b>top 1%</b>
        or <b>top 10%</b> most cited papers of  the world. In case  of India, it is observed that India's contribution to top 1%
        highly cited papers has grown from <b>1.85% in 2010</b> to <b>4.3% in 2019</b>. Similarly, India's contribution in
        top 10% highly cited papers of the world has grown from <b>2.28% in 2010</b> to <b>4.52% in 2019</b>.
        The figure below shows the year-wise percentage contribution of India to the top 1% and top 10% highly cited papers of the world.
